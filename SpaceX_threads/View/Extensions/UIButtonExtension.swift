import UIKit

extension UIButton {
    
    func setAppearance () {
        self.layer.cornerRadius = 8
        self.layer.borderColor = #colorLiteral(red: 0.1529411765, green: 0.1529411765, blue: 0.1529411765, alpha: 1)
        self.layer.borderWidth = 1
    }
    
}
